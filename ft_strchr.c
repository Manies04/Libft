#include "libft.h"

char	*ft_strchr(const char *s, int i)
{
	int	a;

	a = 0;
	while (s[a] != '\0')
	{
		if (s[a] == i)
			return ((char *)(s + a));
		a++;
	}
	if (i == '\0')
		return ((char *)(s + a));
	return (NULL);
}
